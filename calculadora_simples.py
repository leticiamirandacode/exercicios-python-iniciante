print('===Calculadora simples===')
n1=float(input('Digite um numero: '))
n2=float(input('Digite outro numero: '))
soma=n1+n2
subtracao=n1-n2
multiplicacao=n1*n2
divisao=n1/n2
print(f'Resultados:\n Soma: {soma}\n Subtração: {subtracao}\n Multiplicação: {multiplicacao}\n Divisão: {divisao:.3f}')